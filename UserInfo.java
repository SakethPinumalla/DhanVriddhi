package myPackage;

public class UserInfo {
    private String email;
    private int userId;

    public UserInfo() {
        // Empty constructor
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getEmail() {
        return email;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getUserId() {
        return userId;
    }
}
